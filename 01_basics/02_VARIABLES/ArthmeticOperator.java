
public class ArthmeticOperator {

    public static void main(String[] args) {
        //Only int type ??,
        int x = 50, y = 10;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        //Double
        Double z = 3.14;
        System.out.println(z);
        //Float
        float a = 3.144f;
        System.out.println(a);
        System.out.println(x + y + z + a);

    }
}
